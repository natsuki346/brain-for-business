function jsonResponse(body: unknown, status = 200) {
  return new Response(JSON.stringify(body), {
    status,
    headers: { ...corsHeaders, 'Content-Type': 'application/json' },
  })
}

Deno.serve(async (req: Request) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const { selectedTags } = await req.json()

    if (!Array.isArray(selectedTags) || selectedTags.length === 0) {
      return jsonResponse({ error: 'selectedTags is required' }, 400)
    }

    const message = await client.messages.create({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 512,
      system: [
        {
          type: 'text',
          text: SYSTEM_PROMPT,
          cache_control: { type: 'ephemeral' },
        },
      ],
      messages: [{
        role: 'user',
        content: `登録済みタグ: ${selectedTags.join('、')}`,
      }],
    })

    const block = message.content[0]
    if (block.type !== 'text') {
      return jsonResponse({ error: 'unexpected response' }, 500)
    }

    const parsed = JSON.parse(block.text) as { positive: string[]; negative: string[] }
    const alreadyHas = new Set(selectedTags)
    const positive = (parsed.positive ?? []).filter((t: string) => !alreadyHas.has(t))
    const negative = (parsed.negative ?? []).filter((t: string) => !alreadyHas.has(t))

    return jsonResponse({ positive, negative })
  } catch (err) {
    return jsonResponse({ error: err instanceof Error ? err.message : 'unexpected error' }, 500)
  }
})
